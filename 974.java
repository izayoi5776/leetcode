class Solution {
    int K;
    public int subarraysDivByK(int[] A, int K) {
        this.K = K;
        int ret = 0;
        
        if(A!=null && A.length>0 && K!=0){
            int[] sum = new int[A.length]; // sum of A from 0 to i
            int[] ak = new int[K]; // 从0开始的所有子串，其和模K，数量放入ak
            sum[0] = mod(A[0]);
            ak[mod(sum[0])]++;
            // 计算从0开始的所以子串和
            for(int i=1; i<A.length; i++){
                sum[i] = mod(A[i] + sum[i-1]);
                ak[mod(sum[i])]++;
            }
            //System.out.printf("sum=%s ak=%s\n", Arrays.toString(sum), Arrays.toString(ak));
            
            int pos = 0;    // 指向现在表示模K结果为0的那一项
            ret += ak[pos];
            for(int i=1; i<A.length; i++){
                int mpe = mod(A[i-1]);  // 前一个数的模
                ak[mod(pos+mpe)]--;     // 所有从i-1开始的子串，只有一个，即只包含A[i-1]一项的子串不存在了。

                // 从所有子串和中减去 A[i-1], 导致 ak 中的各项余数变化K - mpe
                pos = mod(pos + mpe);
                ret += ak[pos];

                //System.out.printf("i=%d ret=%d mpe=%d ak=%s pos=%d\n", i, ret, mpe, Arrays.toString(ak), pos);
            }
        }
        
        return ret;
    }
    // 把负数也模到非负的 0~k-1 范围里
    int mod(int n){
        return ((n % K) + K) % K;
    }
}
